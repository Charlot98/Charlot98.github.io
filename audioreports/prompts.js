function detectExamTypes(text) {
  const t = text.replace(/\s+/g, '');
  const detected = [];
  for (const [key, def] of Object.entries(EXAM_TYPE_DEFS)) {
    if (!def.keywords || def.keywords.length === 0) continue;
    if (def.keywords.some(kw => t.includes(kw))) detected.push(key);
  }
  return detected;
}

