// 从转录文字中检测匹配的病种
function detectDiseases(text) {
  const t = text.replace(/\s+/g, '');
  return Object.entries(DISEASE_DEFS).filter(([, def]) =>
    def.keywords.some(kw => t.includes(kw))
  );
}

// 将检测到的病种追加到 system prompt
function appendDiseaseHints(basePrompt, transcript) {
  const matched = detectDiseases(transcript);
  if (matched.length === 0) return basePrompt;

  const lines = matched.map(([name, def]) =>
    `${name}（${def.organ}）\n  所见参考：${def.text}\n  结论参考：${def.conclusion}`
  ).join('\n\n');

  return basePrompt +
    `\n\n【检测到相关病种——异常描述参考（口述提及时替换正常模板对应条目）】\n${lines}`;
}
