function _ruleMatchesRestriction(restriction, species, sex) {
  if (!restriction || restriction === '无' || restriction === '不限品种，不限性别') return true;
  const parts = restriction.split('，');
  const speciesPart = parts[0] || '';
  const sexPart = parts[1] || '';
  if (speciesPart !== '不限品种') {
    if (speciesPart.includes('犬') && species !== '犬') return false;
    if (speciesPart.includes('猫') && species !== '猫') return false;
  }
  if (sexPart && sexPart !== '不限性别') {
    if (!sexPart.split('/').includes(sex)) return false;
  }
  return true;
}

function detectDiseaseRules(text, species, sex) {
  const t = text.replace(/\s+/g, '');
  return DISEASE_RULES.filter(([, keyword, , , restriction]) => {
    if (!_ruleMatchesRestriction(restriction, species, sex)) return false;
    const triggers = keyword
      .replace(/（[^）]*）/g, '')
      .split('/')
      .map(s => s.trim())
      .filter(s => s.length > 1);
    return triggers.some(trigger => t.includes(trigger));
  });
}

function appendDiseaseRules(basePrompt, matchedRules) {
  if (!matchedRules || matchedRules.length === 0) return basePrompt;
  const lines = matchedRules.map(([organ, keyword, fragment, conclusion]) => {
    const arrowIdx = fragment.indexOf('→');
    const orig = arrowIdx >= 0 ? fragment.slice(0, arrowIdx) : fragment;
    const newText = arrowIdx >= 0 ? fragment.slice(arrowIdx + 1) : '';
    return `- ${organ}（${keyword}）：将"${orig}"替换为"${newText}"；结论参考：${conclusion}`;
  }).join('\n');
  return basePrompt +
    '\n\n【检测到相关病种——异常描述参考（将对应正常模板中的原文片段替换为新文片段；同一器官的多条结论参考请合并为一条）】\n' + lines;
}

