function _normalizeSubOrgans(subOrgan) {
  if (!subOrgan) return [];
  return (Array.isArray(subOrgan) ? subOrgan : [subOrgan]).filter(Boolean);
}

function _applySingleOrganDefs(organTextMap, subOrgans) {
  for (const chip of subOrgans) {
    const entries = _SINGLE_ORGAN_DEFS[chip];
    if (!entries) continue;
    for (const { name, text } of entries) organTextMap.set(name, text);
  }
}

