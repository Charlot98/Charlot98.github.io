function detectSpecies(text) {
  const t = text.replace(/\s+/g, '');
  if (/猫/.test(t)) return '猫';
  if (/犬|狗/.test(t)) return '犬';
  return null;
}

function detectGender(text) {
  const t = text.replace(/\s+/g, '');
  if (/去势/.test(t)) return '雄性去势';
  if (/绝育/.test(t)) return '雌性绝育';
  if (/雄性|公猫|公犬/.test(t)) return '雄性';
  if (/雌性|母猫|母犬/.test(t)) return '雌性';
  return null;
}

function buildSystemPrompt(types, species, sex, subOrgan) {
  if (!types || types.length === 0) return null;

  const parts = [];
  const subOrgans = _normalizeSubOrgans(subOrgan);

  // 1. 处理有完整模板的特殊类型（AFAST / TFAST / 疝）
  const fullTypes = types.filter(t => EXAM_TYPE_DEFS[t]?.fullTemplate);
  fullTypes.forEach(t => {
    parts.push(`【${species} · ${EXAM_TYPE_DEFS[t].label}】\n${_FULL_TEMPLATES[t](species)}`);
  });

  // 2. 合并套餐检查 + 单腔器官（统一编号模板，仰卧位扫查仅首行出现一次）
  const modularTypes = types.filter(t =>
    !EXAM_TYPE_DEFS[t]?.fullTemplate && t !== '单腔'
  );
  const hasSingle = types.includes('单腔') && subOrgans.length > 0;

  if (modularTypes.length > 0 || hasSingle) {
    const organTextMap = new Map();

    if (modularTypes.length > 0) {
      const organSet = new Set();
      modularTypes.forEach(t => {
        const def = EXAM_TYPE_DEFS[t];
        (def.organs || []).forEach(o => organSet.add(o));
      });

      if (modularTypes.some(t => EXAM_TYPE_DEFS[t]?.hasProstate)) {
        if ((sex === '雄性' || sex === '雄性去势') && species !== '猫') organSet.add('前列腺');
      }
      if (modularTypes.some(t => EXAM_TYPE_DEFS[t]?.hasReproductiveOrgans)) {
        if (sex === '雄性') organSet.add('睾丸');
        else if (sex === '雌性') { organSet.add('卵巢'); organSet.add('子宫'); }
      }
      if (modularTypes.some(t => EXAM_TYPE_DEFS[t]?.hasReproductiveLymph)) {
        if (sex === '雌性') organSet.add('髂内淋巴结');
      }

      for (const organ of organSet) {
        organTextMap.set(organ, _ORGAN_TEXT[organ] || organ);
      }
    }

    if (hasSingle) _applySingleOrganDefs(organTextMap, subOrgans);

    const ordered = ORGAN_ORDER.filter(o => organTextMap.has(o));
    const templateLines = ordered.map((organ, i) =>
      `${i + 1}.[${organ}]${organTextMap.get(organ)}`
    ).join('\n');

    const labelParts = [];
    if (modularTypes.length > 0) {
      labelParts.push(modularTypes.map(t => EXAM_TYPE_DEFS[t].label).join('＋'));
    }
    if (hasSingle) labelParts.push(`单腔（${subOrgans.join('＋')}）`);

    const headerTag = modularTypes.length > 0
      ? `【${species}${sex} · ${labelParts.join('＋')} 正常模板】`
      : `【${species} · ${labelParts.join('＋')}】`;

    parts.push(`${headerTag}\n${species}仰卧位扫查：\n${templateLines}`);
  }

  if (parts.length === 0) return null;

  return `${_BASE_RULES}\n\n${parts.join('\n\n')}\n${_JSON_FORMAT}`;
}

